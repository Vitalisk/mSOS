			<h3><a href="#">What is mSOS?</a></h3>
			<div>
			<p style="font-size: 18px; text-align: justify; width:97%" >
				mSOS stands for mobile SMS based disease outbreak alert system. 
				mSOS aims to improve the timeliness of outbreak notification and improve the 
				lead time to response in order to minimize morbidity and mortality once a suspected outbreak 
				is detected at the health facility. It specifically aims to establish an outbreak alert model 
				that is sustainable and evidence based in order to strengthen real-time notification and response of 
				suspected disease outbreaks, laboratory confirmation, and health workers’ knowledge and skills to detect outbreaks.
			</p>
			
		</div>
		<h3><a href="#">How does mSOS work?</a></h3>
			<div>
			<p style="font-size: 18px; text-align: justify;width:97%">
				Using mSOS. health facility workers can send suspected disease notifications on the reportable 
				diseases using pre-defined SMS formats to a toll-free number. Messages are stored on a central server. 
				Once the epidemic threshold is surpassed, personnel responsible for lab specimen collection, investigation, 
				and response actions at the Ministry 
				of Health will receive an alert message on the suspected incidences notified by the health facility workers.
			</p>
			
		</div>
		<h3><a href="#">What are the reportable diseases under mSOS?</a></h3>
			<div>
			<p style="font-size: 18px; text-align: justify;width:97%">
				There are 14 reportable diseases under the mSOS pilot project. They include: 1. Adverse events following immunization,
				 2. Anthrax, 3. Cholera, 4. Dengue fever, 5. Dysentery (diarrhea with blood, shigella dysentery), 6. Guinea worm disease,
				  7. Measles, 8. Neonatal tetanus, 9. Plague, 10. Rift Valley fever, 11. Severe respiratory illness, 12. Yellow fever, 13. 
				  Viral hemorrhagic fever, 14. Other (any public health event of national or international concern).
			</p>
			
		</div>
		<h3><a href="#">How do I report an outbreak using mSOS?</a></h3>
			<div>
			<p style="font-size: 18px; text-align: justify;width:97%">
				To send a notification on suspected disease incident, send a message with the following format to 
				the toll-free number 22700: alert diseasecode age sex status (example: alert atx 14 m a). Once the message is successfully sent, 
				you will receive the following message: “Message received. Action will be taken promptly. The incident ID is MTA3SF9Y7K”
			</p>
			
		</div>
		<h3><a href="#">What happens after I send a notification?</a></h3>
			<div>
			<p style="font-size: 18px; text-align: justify;width:97%">
				
Messages are stored on the central server and the information will be displayed on the password protected 
web portal. Depending on the epidemic thresholds, SMS messages will be sent directly to appropriate staff 
members at the Ministry of Health. 

			</p>
			
		</div>
		<h3><a href="#">Where do I report technical errors?</a></h3>
			<div>
			<p style="font-size: 18px; text-align: justify;width:97%">
				To query the SMS format on how to send an alert, send a message to the toll-free number 22700 with the following format: format alert.

To query the SMS format on how to edit the name of the disease, send a message to the toll-free number 22700 with the following format: format edit.

To query the SMS format on how to send weekly reports, send a message to the toll-free number 22700 with the following format: format weeksummary.

To edit the name of the disease on a suspected incident, send a SMS with the following format: edit incidentID old_diseasecode new_diseasecode (example: MTA3SF9Y7K atx vhf). Once the message is successfully sent, you will receive the following message: “Incident MTA3SF9Y7K has been changed from anthrax to viral hemorrhagic fever”

If you sent a notification by mistake, please contact your DDSC. If you experience downtime on sending the notification via SMS, please contact your DDSC to send a notification on your behalf through the web portal.

Please contact the mSOS Help Desk if you have any other issues or concerns to report.

			</p>
			
		</div>
		<h3><a href="#">What is weekly SMS confirmation?</a></h3>
			<div>
			<p style="font-size: 18px; text-align: justify;width:97%">
				

To submit weekly summary, send a message to the toll-free number 
22700 with the following message: weeksummary total_number_of_incidences (example: week 10). 
Once the message is successfully sent, you will receive the following message: “Thank you for the correct data. 
/ You have a discrepancy.”


